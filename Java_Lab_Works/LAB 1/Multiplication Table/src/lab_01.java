public class lab_01{
    public static void main(String[] args) {
        int number = 2;
        System.out.println("Multiplication Table of 2:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " x " + i + " = " + (number * i));
        }
        System.out.println("");
    }
}